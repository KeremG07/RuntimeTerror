package Body;

public class Body {
    public int x;
    public int y;
    public int length;
    public int width;
    public Body (int x_coordinates,
                 int y_coordinates,
                 int length,
                 int width){
        x = x_coordinates;
        y = y_coordinates;
        this.length = length;
        this.width = width;
    }
}
